package day04;

public class For3 {
		public static void main(String[] args) {
			
			// #1
			for(int q=0; q<5; q++) {
				System.out.println("안녕");
			}
			
			// #2
			int g=0;
			for(int q=0; q<11; q++) {
				g+=q;
			}
			System.out.println(g);
			
			// #3
			int sum = 0;
			for(int i=0;i<11;i++) {
				sum +=1;
			}
			
			//  #4
			
			for(int k=11; k > 0; k--)
				System.out.println(k);
		}
}
